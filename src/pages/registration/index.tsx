import Head from 'next/head'
import BaseLayout from '@/components/BaseLayout/BaseLayout'
import SignUpHeroBg from '../../../public/assets/join.jpg'
import SignUpForm from '@/features/Sign/SignUpForm/SignUpForm'
import FormLayout from '@/components/FormLayout/FormLayout'
import { GetServerSideProps } from 'next/types';
import { getServerSession } from 'next-auth/next';
import { authOptions } from '@/pages/api/auth/[...nextauth]';

export default function Home() {
  const heroBg = {
    src: SignUpHeroBg,
    alt: 'Stare drzwi zabezpieczone łańczuchem spiętym kłódką.'
  }
  return (
    <>
      <Head>
        <title>Rejestracja</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <BaseLayout image={heroBg} isAlignItemsCenter>
        <FormLayout title='Rejestracja'>
          <SignUpForm/>
        </FormLayout>
      </BaseLayout>
    </>
  )
}

export const getServerSideProps: GetServerSideProps = async ({ req, res }) => {
  const token = await getServerSession(
      req,
      res,
      authOptions
  )
  if (token) return {
      redirect: {
          destination: '/todo',
          permament: false
      },
      props: {}
  }
  else return {
      props: {}
  }
}